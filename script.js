let currentStand = "";
let descriptionText = "";

function filterBtnEvent(index) {
    if (index == 1) {
        currentStand = "lebensmittel1actuelle";
        renderTextContent(lebensmittel1, wirkung1);
    } else if (index == 2) {
        currentStand = "lebensmittel2actuelle";
        renderTextContent(lebensmittel2, wirkung2)
    } else if (index == 3) {
        currentStand = "lebensmittel2actuelle";
        renderTextContent(lebensmittel3, wirkung3);
    }
}

function renderHeaderContent() {
    let headerContentBtn = document.getElementById("header_btn_box")
    headerContentBtn.innerHTML = getHeaderCotentBtn();
}

function getHeaderCotentBtn() {
    return `
    <div class="btn_content_global">
    <button class="btn_style" onclick="filterBtnEvent(1)">Bien pour vous 1</button>
    <button class="btn_style" onclick="filterBtnEvent(2)">Bien pour vous 2</button>
    <button class="btn_style" onclick="filterBtnEvent(3)">Bien pour vous 3</button>
    </div>
    `
}


// Render Text left
function renderTextContent(arrLebensmittel, arrWirkung) {
    let renderTextRef = document.getElementById("text_content_box");
    renderTextRef.innerHTML = "";
    for (let index = 0; index < arrLebensmittel.length; index++) {
        renderTextRef.innerHTML = getRandomText(index, arrLebensmittel, arrWirkung);
    }
}

function getRandomText(i, leben, wirk) {
    return `<div onclick="renderTextinsideOverlay(${i}, '${currentStand}')" onclick="toggleOverlay" class ="text_content_box">
    <h2>${leben[i]}</h2>
    <h3>${wirk[i]}
    </div>
    `
}
//====================================================================================


function init() {
    renderHeaderContent()
    renderTextContent()
}